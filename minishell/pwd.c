#include "minishell.h"

int		ft_pwd()
{
	char cwd[PATH_MAX];
	getcwd(cwd, PATH_MAX);
	ft_putstr_fd(cwd, 1);
	write(1, "\n", 1);
	return (g_data.ret = 0);
}
